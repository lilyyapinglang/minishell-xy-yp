#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*un_s1;
	unsigned char	*un_s2;

	un_s1 = (unsigned char *)s1;
	un_s2 = (unsigned char *)s2;
	i = 0;
	while (i < n)
	{
		if ((un_s1[i] != un_s2[i]))
			return (un_s1[i] - un_s2[i]);
		i++;
	}
	return (0);
}
